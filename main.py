class ErrorApp(BoxLayout):

    def __init__(self, **kwargs):
        super(ErrorApp, self).__init__(**kwargs)
        self.orientation ='vertical'
        Window.size = (360, 260)
        # Dynamically add the font file path
        resource_add_path(os.path.dirname(__file__))
        # 將kivy預設的字體替換成指定的中文字體
        LabelBase.register('Roboto','fonts/NotoSerifTC-VariableFont_wght.ttf')
        # 標籤用於說明輸入框的作用
        self.label = Label(text="請輸入錯誤代碼:", size_hint_y=None, height=50)
        self.add_widget(self.label)

        # 文本輸入框用於輸入錯誤代碼
        self.text_input = TextInput(multiline=False, size_hint_y=None, height=50)
        self.add_widget(self.text_input)

        # 按鈕用於觸發查找
        self.button = Button(
            text="查找錯誤原因",
            size_hint_y=None,  # 禁用自動調整高度
            height=30  # 設定按鈕高度
        )
        self.button.bind(on_press=self.find_error_reason)
        # Bind the Enter key to the button
        Window.bind(on_key_down=self.on_key_down)
        self.add_widget(self.button)

        # 使用 ScrollView 包含顯示錯誤原因的 TextInput
        self.scroll_view = ScrollView(size_hint=(1, 1))
        self.result_input = TextInput(
            text="",
            size_hint_y=None,
            readonly=True,  # 設為只讀
            background_color=(0, 0, 0, 0),  # 透明背景
            foreground_color=(1, 1, 1, 1),  # 白色字體
            cursor_color=(1, 1, 1, 1),  # 白色游標
            multiline=True
        )

        self.scroll_view.add_widget(self.result_input)
        self.add_widget(self.scroll_view)

    def on_key_down(self, window, key, *args):
        if key == 13:  # Enter key
            self.button.trigger_action(0)

    def find_error_reason(self, instance):
        # 使用字典推導式將鍵轉換為大寫合併
        upper_case_dict = {key.upper(): value for key, value in error_dict.items()}
        upper_case_dict.update({key.upper(): value for key, value in error_dict1.items()})
        error_code = self.text_input.text.strip().upper()
        reason = upper_case_dict.get(error_code, "未找到對應的錯誤原因")
        try:
            translator = GoogleTranslator(source='auto', target='zh-TW')
            translated = translator.translate(reason)
            self.result_input.text = translated
        except:
            self.result_input.text = reason

        # Adjust the height of result_input based on its content
        self.result_input.height = max(30, self.result_input.minimum_height)


class VBSErrorApp(App):
    title = 'Oracle Error 查詢系統'
    def build(self):
        return ErrorApp()


